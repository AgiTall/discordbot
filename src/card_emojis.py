SUIT_NAMES = {"♠": "spades", "♥": "hearts", "♦": "diamonds", "♣": "clubs"}


def format_card_emoji(card) -> str:
    """Return custom emoji markup, falling back to ordinary card text."""
    rank, suit = card
    emoji_id = CARD_EMOJI_IDS.get(suit, {}).get(rank)
    if not emoji_id:
        return f"{rank}{suit}"
    return f"<:{SUIT_NAMES[suit]}_{rank.lower()}:{emoji_id}>"
